def is_trading_day(d: date) -> bool:
    """判断是否为A股交易日（排除周末和非交易日）"""
    date_str = d.strftime('%Y%m%d')
    if date_str in NON_TRADING_DAYS_2026:
        return False
    if d.weekday() >= 5:  # 周六、周日
        return False
    return True
