# 调休上班日（周末但当交易日），优先级最高
TRADING_DAYS_EXCEPTIONAL = {
    '20260228',  # 周六调休上班
    '20260301',  # 周日调休上班
}


def is_trading_day(d=None):
    """判断是否为A股交易日

    优先级：
    1. 调休上班日（TRADING_DAYS_EXCEPTIONAL）→ 交易日
    2. 非交易日（NON_TRADING_DAYS_2026）→ 非交易日
    3. 周末（weekday >= 5）→ 非交易日
    4. 其他 → 交易日
    """
    if d is None:
        d = date.today()
    date_str = d.strftime('%Y%m%d')
    if date_str in TRADING_DAYS_EXCEPTIONAL:
        return True
    if date_str in NON_TRADING_DAYS_2026:
        return False
    if d.weekday() >= 5:
        return False
    return True


def is_non_trading_day(d=None):
    """判断是否为非交易日（周末+节假日）"""
    return not is_trading_day(d)


def is_market_open(d=None):
    """判断当前是否在交易时间内"""
    now = datetime.now()
    if not is_trading_day(d):
        return False
    h, m = now.hour, now.minute
    return (h == 9 and m >= 30) or (h == 10) or (h == 11 and m < 30) or (h == 13) or (h == 14) or (h == 15 and m < 5)


def is_post_market(d=None):
    """盘后（15:05-16:00）"""
    now = datetime.now()
    if not is_trading_day(d):
        return False
    h, m = now.hour, now.minute
    return (h == 15 and m >= 5) or h == 16
